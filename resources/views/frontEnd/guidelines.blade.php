                                    <h2 class="stageCount">Pros and Cons </h2>
                                    <h1>Diversified Portfolio</h1>
                                    <p>
                                        A well-diversified portfolio can help cover all the bases and safeguards your investments especially during recessions. Diversification
                                        shields an investor from daily market volatility and reduces overall risk. When investments in Stock A shows poor returns, Stock B in the
                                        portfolio can counterbalance losses. Therefore, it is advisable to hold assets that are negatively correlated with one another.
                                    </p>
                                    <p>
                                        However, diversification can also have opposing effects on your portfolio. For instance, an investment portfolio of overdiversified stocks
                                        can significantly reduce the returns to average, even when certain sectors in the market booming. Besides, a widely diversified portfolio
                                        from separate asset classes is generally more trouble to monitor and adjust since the investor has to stay on top of many investments.
                                    </p>
                                </div>

